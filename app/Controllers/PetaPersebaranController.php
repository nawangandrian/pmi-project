    /**
     * API: rekap jumlah pendonor per kecamatan per golongan darah
     * Hanya golongan darah yang memiliki data yang akan di-include
     * GET /peta-persebaran/data
     */
    public function getData()
    {
        $db   = \Config\Database::connect();
        $rows = $db->table('data_pendonor')
            ->select('kecamatan, golongan_darah, COUNT(*) AS jumlah')
            ->groupBy(['kecamatan', 'golongan_darah'])
            ->orderBy('kecamatan', 'ASC')
            ->get()->getResultArray();

        $result = [];
        foreach ($rows as $row) {
            $kec = $this->normalizeKec($row['kecamatan']);
            if (! isset($result[$kec])) {
                $result[$kec] = ['kecamatan' => $kec, 'golongan' => [], 'total' => 0];
            }
            $result[$kec]['golongan'][$row['golongan_darah']] = (int) $row['jumlah'];
            $result[$kec]['total'] += (int) $row['jumlah'];
        }

        return $this->response->setJSON(array_values($result));
    }

    /**
     * API: daftar golongan darah yang aktif (ada datanya) + statistik global
     * GET /peta-persebaran/golongan-aktif
     */
    public function getGolonganAktif()
    {
        $db = \Config\Database::connect();

        $rows = $db->table('data_pendonor')
            ->select('golongan_darah, COUNT(*) AS jumlah')
            ->groupBy('golongan_darah')
            ->orderBy('jumlah', 'DESC')
            ->get()->getResultArray();

        $aktif = [];
        foreach ($rows as $row) {
            if ((int) $row['jumlah'] > 0) {
                $aktif[$row['golongan_darah']] = (int) $row['jumlah'];
            }
        }

        return $this->response->setJSON([
            'golongan_aktif' => $aktif,   // [ 'A+' => 120, 'B+' => 98, ... ]
            'semua_golongan' => self::ALL_GOL,
        ]);
    }

    /**
     * API: statistik + top kecamatan
     * GET /peta-persebaran/statistik
     */
    public function getStatistik()
    {
        $db = \Config\Database::connect();

        $rawTop = $db->table('data_pendonor')
            ->select('kecamatan, COUNT(*) AS jumlah')
            ->groupBy('kecamatan')
            ->orderBy('jumlah', 'DESC')
            ->get()->getResultArray();

        // Merge setelah normalisasi
        $merged = [];
        foreach ($rawTop as $r) {
            $kec = $this->normalizeKec($r['kecamatan']);
            $merged[$kec] = ($merged[$kec] ?? 0) + (int) $r['jumlah'];
        }
        arsort($merged);

        $top = array_slice(
            array_map(
                fn($k, $v) => ['kecamatan' => $k, 'jumlah' => $v],
                array_keys($merged),
                $merged
            ),
            0, 3
        );

        $perGolongan = $db->table('data_pendonor')
            ->select('golongan_darah, COUNT(*) AS jumlah')
            ->groupBy('golongan_darah')
            ->orderBy('jumlah', 'DESC')
            ->get()->getResultArray();

        return $this->response->setJSON([
            'total'          => $db->table('data_pendonor')->countAll(),
            'per_golongan'   => $perGolongan,
            'top_kecamatan'  => array_values($top),
        ]);
    }

