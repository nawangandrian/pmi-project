    // ─────────────────────────────────────────────────────────────────────────
    private function normalizeKec(string $raw): string
    {
        $kec = ucwords(strtolower(trim($raw)));
        if (stripos($kec, 'kota') !== false) {
            return 'Kota';
        }
        return $kec;
    }
